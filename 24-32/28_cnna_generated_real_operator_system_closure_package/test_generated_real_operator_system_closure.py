"""
CNNA / growing real complement network
Generated real operator-system closure diagnostic.

Purpose
-------
This diagnostic follows the Record/Live block adjoint test.  The previous test
showed that the immediate growth-visible reverse handoff family does not close
under the DtN block adjoint, even though the adjoint lies in a larger typed
rank-one envelope.

This test therefore builds the finite real operator system generated by the
actual longitudinal and transverse Record/Live block operators.  For a local
Double-History pair a,b it works on the two-cell block space

    X_ab = W_a ⊕ W_b,     W_p = B_p^record ⊕ B_p^live,     B_p = R^3.

The growth-produced generators are embedded as 12x12 real matrices:

    D_ab      = diag(L_a, L_b),
    N_ab^k   = off-diagonal handoff W_a -> W_b,
    N_ba^k   = off-diagonal handoff W_b -> W_a,
    I_ab      = identity.

The test closes the real linear span under words up to a finite degree
(default 1,2,3), then asks:

  1. Is the generated finite word-space stable under the block DtN adjoint?
  2. Is it stable, at this truncation, under one more multiplication by the
     growth generators?
  3. Does true growth behave differently from identical-history, symmetrized,
     no-backreaction, and random controls?

This is still not a J-test.  It is a pre-* gate: a real local operator-system
closure diagnostic.
"""

