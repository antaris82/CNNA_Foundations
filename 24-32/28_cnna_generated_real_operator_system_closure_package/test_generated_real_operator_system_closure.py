def matrix_rank_span(mats: List[np.ndarray], tol: float) -> int:
    good = [np.asarray(M, dtype=float).reshape(-1) for M in mats if np.linalg.norm(M) > tol]
    if not good:
        return 0
    A = np.stack(good, axis=1)
    s = np.linalg.svd(A, compute_uv=False)
    return int(np.sum(s > tol * max(A.shape) * max(float(s[0]), 1.0)))


def orthonormal_span(mats: List[np.ndarray], tol: float) -> Tuple[np.ndarray, List[np.ndarray], List[float]]:
    good = [np.asarray(M, dtype=float).reshape(-1) for M in mats if np.linalg.norm(M) > tol]
    if not good:
        return np.zeros((0, 0), dtype=float), [], []
    A = np.stack(good, axis=1)
    U, s, _ = np.linalg.svd(A, full_matrices=False)
    if len(s) == 0:
        return np.zeros((A.shape[0], 0), dtype=float), [], []
    keep = s > tol * max(A.shape) * max(float(s[0]), 1.0)
    Q = U[:, keep]
    dim = int(np.sqrt(A.shape[0]))
    basis = [Q[:, i].reshape(dim, dim) for i in range(Q.shape[1])]
    return Q, basis, [float(x) for x in s]


def residual_to_Q(M: np.ndarray, Q: np.ndarray) -> Tuple[float, float]:
    v = np.asarray(M, dtype=float).reshape(-1)
    n = float(np.linalg.norm(v))
    if n < EPS:
        return 0.0, 0.0
    if Q.size == 0 or Q.shape[1] == 0:
        return n, 1.0
    proj = Q @ (Q.T @ v)
    r = v - proj
    rn = float(np.linalg.norm(r))
    return rn, rn / (n + EPS)


def embed_diag(A: np.ndarray, B: np.ndarray) -> np.ndarray:
    Z = np.zeros_like(A)
    return np.block([[A, Z], [Z, B]])


def embed_ab(T: np.ndarray) -> np.ndarray:
    Z = np.zeros_like(T)
    return np.block([[Z, Z], [T, Z]])


def embed_ba(T: np.ndarray) -> np.ndarray:
    Z = np.zeros_like(T)
    return np.block([[Z, T], [Z, Z]])


def build_pair_generators(
    a: LocalCell,
    b: LocalCell,
    operator_mode: str,
    longitudinal_mode: str,
    generator_ports: str,
    include_projectors: bool,
    include_longitudinal_powers: bool,
) -> Tuple[List[np.ndarray], Dict[str, float]]:
    La = block_longitudinal(a, longitudinal_mode)
    Lb = block_longitudinal(b, longitudinal_mode)
    I6 = np.eye(6)
    Z6 = np.zeros((6, 6), dtype=float)
    I12 = np.eye(12)
    Pa = np.block([[I6, Z6], [Z6, Z6]])
    Pb = np.block([[Z6, Z6], [Z6, I6]])
    D = embed_diag(La, Lb)
    gens: List[np.ndarray] = [I12, D]
    if include_projectors:
        gens.extend([Pa, Pb])
    if include_longitudinal_powers:
        gens.append(D @ D)
    ports = [1, 2, 3]
    if generator_ports == "same_port_only":
        # caller still passes pair port through a/b grouping in rows; here all pairs
        # are evaluated with the same selected port stored in stats['selected_port'].
        raise ValueError("same_port_only handled by row_for_pair")
    for r in ports:
        Tab = block_handoff(a, b, r, operator_mode)[0]
        Tba = block_handoff(b, a, r, operator_mode)[0]
        gens.append(embed_ab(Tab))
        gens.append(embed_ba(Tba))
    stats = {
        "La_norm": fro(La),
        "Lb_norm": fro(Lb),
        "D_norm": fro(D),
        "seed_count": len(gens),
    }
    return gens, stats


def build_pair_generators_same_port(
    a: LocalCell,
    b: LocalCell,
    port: int,
    operator_mode: str,
    longitudinal_mode: str,
    include_projectors: bool,
    include_longitudinal_powers: bool,
) -> Tuple[List[np.ndarray], Dict[str, float]]:
    La = block_longitudinal(a, longitudinal_mode)
    Lb = block_longitudinal(b, longitudinal_mode)
    I6 = np.eye(6)
    Z6 = np.zeros((6, 6), dtype=float)
    I12 = np.eye(12)
    Pa = np.block([[I6, Z6], [Z6, Z6]])
    Pb = np.block([[Z6, Z6], [Z6, I6]])
    D = embed_diag(La, Lb)
    gens: List[np.ndarray] = [I12, D]
    if include_projectors:
        gens.extend([Pa, Pb])
    if include_longitudinal_powers:
        gens.append(D @ D)
    Tab = block_handoff(a, b, port, operator_mode)[0]
    Tba = block_handoff(b, a, port, operator_mode)[0]
    gens.append(embed_ab(Tab))
    gens.append(embed_ba(Tba))
    return gens, {"La_norm": fro(La), "Lb_norm": fro(Lb), "D_norm": fro(D), "seed_count": len(gens)}


def word_space(gens: List[np.ndarray], max_degree: int, tol: float, cap: int) -> Tuple[List[np.ndarray], Dict[str, float]]:
    # Build all generator words up to degree max_degree, compressing span after
    # every degree to keep the diagnostic finite and reproducible.
    words: List[np.ndarray] = [gens[0]] if gens else []
    current: List[np.ndarray] = [gens[0]] if gens else []
    all_mats: List[np.ndarray] = list(gens)
    degree_counts = {"degree_0_or_seed_count": len(all_mats)}
    prev_words = list(gens)
    for d in range(2, max_degree + 1):
        new_words: List[np.ndarray] = []
        for A in prev_words:
            for G in gens:
                new_words.append(A @ G)
                if len(new_words) >= cap:
                    break
            if len(new_words) >= cap:
                break
        all_mats.extend(new_words)
        degree_counts[f"degree_{d}_raw_words"] = len(new_words)
        Q, basis, _ = orthonormal_span(all_mats, tol)
        # Use current basis as prev_words for next degree to avoid exponential blowup.
        prev_words = basis[:cap]
        all_mats = basis[:cap]
        degree_counts[f"degree_{d}_span_dim"] = len(all_mats)
    Q, basis, svals = orthonormal_span(all_mats, tol)
    stats = dict(degree_counts)
    stats["word_space_dim"] = int(Q.shape[1]) if Q.size else 0
    stats["word_space_sval_max"] = max(svals, default=0.0)
    stats["word_space_sval_min_kept"] = min([x for x in svals if x > tol], default=0.0)
    return basis, stats


def block_pair_metric(a: LocalCell, b: LocalCell, metric_source: str, ridge: float) -> Tuple[np.ndarray, Dict[str, float]]:
    Ga, sa = block_metric(a, metric_source, ridge)
    Gb, sb = block_metric(b, metric_source, ridge)
    G = np.block([[Ga, np.zeros_like(Ga)], [np.zeros_like(Gb), Gb]])
    return G, {
        "metric_a_cond": sa["metric_cond"],
        "metric_b_cond": sb["metric_cond"],
        "metric_a_min_eig": sa["metric_min_eig"],
        "metric_b_min_eig": sb["metric_min_eig"],
        "metric_a_ridge_added": sa["metric_ridge_added"],
        "metric_b_ridge_added": sb["metric_ridge_added"],
    }


def star_relative_stats(basis: List[np.ndarray], Q: np.ndarray, G: np.ndarray) -> Dict[str, float]:
    if not basis:
        return {"star_basis_rel_mean": float("nan"), "star_basis_rel_max": float("nan"), "star_basis_rel_p95": float("nan")}
    rels = []
    for A in basis:
        Astar = adjoint(A, G, G)
        _, rel = residual_to_Q(Astar, Q)
        rels.append(rel)
    return {
        "star_basis_rel_mean": mean(rels),
        "star_basis_rel_std": std(rels),
        "star_basis_rel_p50": perc(rels, 50),
        "star_basis_rel_p95": perc(rels, 95),
        "star_basis_rel_max": max(rels, default=float("nan")),
        "star_basis_rel_lt_0p05_fraction": mean([float(x < 0.05) for x in rels]),
        "star_basis_rel_lt_0p25_fraction": mean([float(x < 0.25) for x in rels]),
    }


def seed_star_stats(gens: List[np.ndarray], Q: np.ndarray, G: np.ndarray) -> Dict[str, float]:
    rels = []
    for A in gens:
        Astar = adjoint(A, G, G)
        _, rel = residual_to_Q(Astar, Q)
        rels.append(rel)
    return {
        "star_seed_rel_mean": mean(rels),
        "star_seed_rel_p95": perc(rels, 95),
        "star_seed_rel_max": max(rels, default=float("nan")),
        "star_seed_rel_lt_0p05_fraction": mean([float(x < 0.05) for x in rels]),
        "star_seed_rel_lt_0p25_fraction": mean([float(x < 0.25) for x in rels]),
    }


def multiplication_stability_stats(gens: List[np.ndarray], basis: List[np.ndarray], Q: np.ndarray, sample_cap: int, seed: int) -> Dict[str, float]:
    if not basis or not gens:
        return {"mult_left_rel_mean": float("nan"), "mult_left_rel_max": float("nan")}
    pairs = [(i, j) for i in range(len(gens)) for j in range(len(basis))]
    rng = random.Random(seed)
    if len(pairs) > sample_cap:
        pairs = rng.sample(pairs, sample_cap)
    rels = []
    for i, j in pairs:
        _, rel = residual_to_Q(gens[i] @ basis[j], Q)
        rels.append(rel)
    return {
        "mult_left_samples": len(rels),
        "mult_left_rel_mean": mean(rels),
        "mult_left_rel_std": std(rels),
        "mult_left_rel_p50": perc(rels, 50),
        "mult_left_rel_p95": perc(rels, 95),
        "mult_left_rel_max": max(rels, default=float("nan")),
        "mult_left_rel_lt_0p05_fraction": mean([float(x < 0.05) for x in rels]),
        "mult_left_rel_lt_0p25_fraction": mean([float(x < 0.25) for x in rels]),
    }


def row_for_pair(
    a: LocalCell,
    b: LocalCell,
    port: int,
    control: str,
    pair_index: int,
    operator_mode: str,
    longitudinal_mode: str,
    metric_source: str,
    generator_ports: str,
    max_degree: int,
    ridge: float,
    tol: float,
    cap: int,
    sample_cap: int,
    seed: int,
    include_projectors: bool,
    include_longitudinal_powers: bool,
) -> Dict[str, float | str | int]:
    if generator_ports == "same_port_only":
        gens, gen_stats = build_pair_generators_same_port(a, b, port, operator_mode, longitudinal_mode, include_projectors, include_longitudinal_powers)
    else:
        gens, gen_stats = build_pair_generators(a, b, operator_mode, longitudinal_mode, generator_ports, include_projectors, include_longitudinal_powers)
    basis, wstats = word_space(gens, max_degree, tol, cap)
    Q, basis2, svals = orthonormal_span(basis, tol)
    G, mstats = block_pair_metric(a, b, metric_source, ridge)
    star_b = star_relative_stats(basis2, Q, G)
    star_s = seed_star_stats(gens, Q, G)
    mult = multiplication_stability_stats(gens, basis2, Q, sample_cap, seed + pair_index)
    Tab = block_handoff(a, b, port, operator_mode)[0]
    Tba = block_handoff(b, a, port, operator_mode)[0]
    La = block_longitudinal(a, longitudinal_mode)
    Lb = block_longitudinal(b, longitudinal_mode)
    Cab = Lb @ Tab - Tab @ La
    # embedded same-port objects, for direct visibility in generated span
    eTab = embed_ab(Tab)
    eTba = embed_ba(Tba)
    D = embed_diag(La, Lb)
    _, T_visible = residual_to_Q(eTab, Q)
    _, Tba_visible = residual_to_Q(eTba, Q)
    _, D_visible = residual_to_Q(D, Q)
    return {
        "control": control,
        "pair_index": pair_index,
        "operator_mode": operator_mode,
        "longitudinal_mode": longitudinal_mode,
        "metric_source": metric_source,
        "generator_ports": generator_ports,
        "max_degree": max_degree,
        "parent_a": a.parent,
        "parent_b": b.parent,
        "port": port,
        "level": a.level,
        "age_a": a.age,
        "age_b": b.age,
        "root_sector_a": a.root_sector,
        "root_sector_b": b.root_sector,
        "suffix": ".".join(map(str, a.suffix)),
        "T_norm": fro(Tab),
        "T_reverse_norm": fro(Tba),
        "C_norm": fro(Cab),
        "embedded_T_visible_relative": T_visible,
        "embedded_Tba_visible_relative": Tba_visible,
        "embedded_D_visible_relative": D_visible,
        **gen_stats,
        **wstats,
        "orthonormal_basis_dim": len(basis2),
        "span_sval_max": max(svals, default=0.0),
        "span_sval_min": min(svals, default=0.0) if svals else 0.0,
        **mstats,
        **star_b,
        **star_s,
        **mult,
    }


def summarize(rows: List[dict], label: str) -> dict:
    out: Dict[str, float | str | int] = {"label": label, "count": len(rows)}
    keys = [
        "T_norm", "T_reverse_norm", "C_norm", "word_space_dim", "orthonormal_basis_dim",
        "star_basis_rel_mean", "star_basis_rel_p50", "star_basis_rel_p95", "star_basis_rel_max",
        "star_basis_rel_lt_0p05_fraction", "star_basis_rel_lt_0p25_fraction",
        "star_seed_rel_mean", "star_seed_rel_p95", "star_seed_rel_max",
        "star_seed_rel_lt_0p05_fraction", "star_seed_rel_lt_0p25_fraction",
        "mult_left_rel_mean", "mult_left_rel_p50", "mult_left_rel_p95", "mult_left_rel_max",
        "mult_left_rel_lt_0p05_fraction", "mult_left_rel_lt_0p25_fraction",
        "metric_a_cond", "metric_b_cond", "metric_a_min_eig", "metric_b_min_eig",
    ]
    for k in keys:
        vals = [r[k] for r in rows if k in r]
        out[f"{k}_mean"] = mean(vals)
        out[f"{k}_std"] = std(vals)
        out[f"{k}_p50"] = perc(vals, 50)
        out[f"{k}_p95"] = perc(vals, 95)
        finite = [float(v) for v in vals if np.isfinite(float(v))]
        out[f"{k}_max"] = max(finite, default=float("nan"))
    out["fraction_nonzero_T"] = mean([float(r["T_norm"] > 1e-10) for r in rows]) if rows else 0.0
    out["fraction_nonzero_C"] = mean([float(r["C_norm"] > 1e-10) for r in rows]) if rows else 0.0
    return out


def truncate_pairs(pairs: List[Tuple[LocalCell, LocalCell, int]], max_pairs: int, seed: int) -> List[Tuple[LocalCell, LocalCell, int]]:
    if max_pairs <= 0 or len(pairs) <= max_pairs:
        return pairs
    rng = random.Random(seed)
    return rng.sample(pairs, max_pairs)


def run_case(case_name: str, model: RealGrowth, args: argparse.Namespace, seed_offset: int) -> Tuple[List[dict], List[dict]]:
    cells = build_cells(model, args.max_level)
    dh = truncate_pairs(double_history_pairs(cells), args.max_pairs_per_control, args.seed + seed_offset)
    ident = truncate_pairs(identical_pairs(cells), args.max_pairs_per_control, args.seed + seed_offset + 101)
    rand = random_pairs(cells, min(args.max_pairs_per_control if args.max_pairs_per_control > 0 else len(dh), max(1, len(dh))), args.seed + seed_offset + 202)
    pair_sets = [
        ("double_history_suffix_quotient", dh),
        ("identical_history_control", ident),
        ("random_same_level_port_baseline", rand),
    ]
    all_rows: List[dict] = []
    summaries: List[dict] = []
    for operator_mode in args.operator_modes:
        for longitudinal_mode in args.longitudinal_modes:
            for metric_source in args.metric_sources:
                for generator_ports in args.generator_ports:
                    for deg in args.degrees:
                        for control, pairs in pair_sets:
                            label = f"{case_name}:{control}:{operator_mode}:{longitudinal_mode}:{metric_source}:{generator_ports}:degree{deg}"
                            rows = [
                                row_for_pair(
                                    a, b, port,
                                    f"{case_name}:{control}",
                                    i,
                                    operator_mode,
                                    longitudinal_mode,
                                    metric_source,
                                    generator_ports,
                                    deg,
                                    args.ridge,
                                    args.tol,
                                    args.word_cap,
                                    args.mult_sample_cap,
                                    args.seed + seed_offset,
                                    args.include_projectors,
                                    args.include_longitudinal_powers,
                                )
                                for i, (a, b, port) in enumerate(pairs)
                            ]
                            all_rows.extend(rows)
                            summaries.append(summarize(rows, label))
    summaries.insert(0, {
        "label": f"{case_name}:meta",
        "count": len(cells),
        "completed_cells": len(cells),
        "double_history_pairs_used": len(dh),
        "identical_pairs_used": len(ident),
        "random_pairs_used": len(rand),
        "nodes": len(model.nodes),
    })
    return all_rows, summaries


def find_summary(summaries: List[dict], label: str) -> dict | None:
    for s in summaries:
        if s.get("label") == label:
            return s
    return None


