def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--max-levels", type=int, nargs="+", default=[5, 6])
    ap.add_argument("--mode", default="linear", choices=["linear", "log", "saturating"])
    ap.add_argument("--controls", nargs="+", default=["real_growth", "symmetrized_birth", "no_backreaction"])
    ap.add_argument("--sources", nargs="+", default=["live", "handoff", "aging"])
    ap.add_argument("--reductions", nargs="+", default=["full", "diagonal", "trace_scalar", "common_mean_diagonal"])
    ap.add_argument("--random-tetras", type=int, default=80)
    ap.add_argument("--max-tetras", type=int, default=0)
    ap.add_argument("--seed", type=int, default=12345)
    ap.add_argument("--min-omega", type=float, default=1e-12)
    ap.add_argument("--loop-step", type=float, default=0.04)
    ap.add_argument("--include-identical-clone", action="store_true", default=True)
    ap.add_argument("--out-prefix", default="tetrahedral_boundary_orientation_out")
    args = ap.parse_args()
    base = Path(__file__).resolve().parent
    for L in args.max_levels:
        run_one(args, int(L), base / f"{args.out_prefix}_L{L}")
    make_readme(base)
    zip_path = base / "tetrahedral_boundary_orientation_outputs.zip"
    if zip_path.exists():
        zip_path.unlink()
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zf:
        for L in args.max_levels:
            out_dir = base / f"{args.out_prefix}_L{L}"
            for p in out_dir.rglob("*"):
                if p.is_file():
                    zf.write(p, p.relative_to(base))


if __name__ == "__main__":
    main()
